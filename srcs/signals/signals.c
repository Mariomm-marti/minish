#include <stdio.h>
#include <signal.h>
#include <stdlib.h>
#include <unistd.h>
#include <utils.h>
#include <libft.h>
#include <utils.h>
#include <readline/readline.h>

void	handler(int sig)
{
	if (sig == SIGINT)
	{
		write(1, "\n", 1);
		rl_replace_line("", 0);
		rl_on_new_line();
		rl_redisplay();
	}
	else if (sig == SIGQUIT)
		write(1, "", 0);
	utils_update_var("?", "130");
}
